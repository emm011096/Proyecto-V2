    void cargar(){
        String[] tabla={"id_Jugador","Nombre","Puntaje"};
        String [] registros=new String[3];
        
        String sql="SELECT * FROM datos order by Puntaje desc";
        model=new DefaultTableModel(null, tabla);
        
        ConexionBD cc=new ConexionBD();
        Connection cn=cc.conexion();
        
        Statement st;
    try {
        st = cn.createStatement();
        ResultSet rs=st.executeQuery(sql);
        
        while (rs.next()){
            registros[0]=rs.getString("id_Jugador");
            registros[1]=rs.getString("Nombre");
            registros[2]=rs.getString("Puntaje");
            model.addRow(registros);
        }
        tablaJugadores.setModel(model);
    } catch (SQLException ex) {
       JOptionPane.showMessageDialog(null, ex);
    }
        
    
        
    }
